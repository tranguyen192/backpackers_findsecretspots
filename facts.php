<div class="content_facts" id="facts">
    <h1> GOOD TO KNOW </h1>

    <p>Southeast Asia is one of the world’s top regions for travellers of all budgets,
      ages and kinds. With cheap places to stay, a tonne of tasty foods to discover and
      new cultures to learn about, there is no surprise that millions flock to the region
      with their backpack every year. Before heading there yourself, it is important
      to brush up on the local customs, laws and norms. So here are some
        things you should <b>not</b> do on your travels through Southeast Asia.</p>

    <h2>Do not touch a person's head</h2>
    <img src="img_website/monk.jpg" alt="monk" class="image_QandA">

    <p>Southeast Asia is home to a number of countries of which Buddhism is the most
      popular religion. In Buddhism, the head is considered sacred, and so you should
      avoid touching the heads of people you are not familiar with, as it could end up
      causing offence.</p>

    <h2>Do not disrespect Gods</h2>
    <img src="img_website/gods.jpg" alt="gods" class="image_QandA">

    <p>Whether it is Buddha, Allah or any other God, much like you would at home, be careful
      not to disrespect the Gods or religions of the countries you are travelling through.
      Religion is a large part of people’s lives and things could turn pretty nasty.
      Make sure not to climb on monuments depicting Gods, and in case of Buddhism
      always present yourself below the Buddha statue, not above it.</p>

    <h2>Do not talk about the Monarchy</h2>
    <img src="img_website/monarchy.jpg" alt="monarchy" class="image_QandA">

    <p>Southeast Asia is home to four countries with a monarchy – Thailand, Brunei,
      Malaysia and Cambodia. As a rule of thumb, it is best to avoid talking about them
      at all. In countries like Thailand, the king is fiercely loved by his subjects, and
      there are strict lese majeste laws that could see you in serious trouble if you say
      something that was perceived to be a negative comment.</p>

    <h2>Do not get involved with Drugs</h2>

    <p>I would like to think this is sound advice for any time in your life, but it is
      especially true in Southeast Asia. From paying huge bribes to being arrested
      for a long time, to even receiving the death penalty, drugs are bad news in
      Southeast Asia so be very careful.</p>

    <h2>Do not put the toilet paper in the toilet</h2>
    <img src="img_website/toilet.jpg" alt="toilet" class="image_QandA">

    <p>Many toilet systems in Southeast Asia simply are not built to cope with paper
      being flushed down the toilet, which can leads to possible awkward moments and
      expensive fines down the way. While some places may have modern toilet and plumbing
      systems, you should be wise to play it safe and put it in the bin provided.</p>

    <h2>Do not shout</h2>

    <p>Whether it is in the street or to a person, shouting is not a smart thing to do.
      If you are upset or in a confrontation, playing it cool and smiling is the best
      way to deal with any potential problems and prevents them from escalating. Also,
      shouting in the street is a great way to attract the wrong kind of attention.</p>

    <h2>Do not dress inappropriately</h2>

    <p>I know that you are on holiday, but it is important
      to remain modest. Buddhist and Muslim countries are modest and respectful when it
      comes to dressing, so try do as the local do. It is also important to remember that
      when you are in a temple, make sure you’re covered up shoulders and knees or you will risk being turned away.</p>

    <h2>Do not ride in a taxi without a meter</h2>
    <img src="img_website/taxi.jpg" alt="taxi" class="image_QandA">

    <p>Taxi drivers love to prey on those who are new to a place and charge them
      extortionate amounts for a journey that should not cost more than your spare
      change. That is why, it is important to demand that they turn on the meter – not
      only will you pay less, you will also encourage the drivers to use meters in the
      future for fear of losing out on custom. A traveller revolution!</p>

    <h2>Do not point your feet to people</h2>

    <p>While the head is considered sacred in Buddhist culture, the feet are considered
      the lowest point. As a result, you should take care not to point your bare feet to
      people as doing so is considered extremely rude. Be careful when putting you are
      feet up!</p>

    <h2>...and do not point at people with your hands!</h2>
    <img src="img_website/finger.jpg" alt="taxi" class="image_QandA">

    <p>Like it is back home, pointing with your hands in Southeast Asia is considered
      rude and impolite. Usually, it is done in an accusatory manner, and it is not
      uncommon to see newspapers with victims pointing at arrested criminals – so bare
      that in mind before you point, even if it is done to show someone the best street
      food in town.</p>

    <h2>Do not use your left hand</h2>
    <img src="img_website/hand.jpg" alt="taxi" class="image_QandA">

    <p>As you may have already noticed, in many Southeast Asian countries there is
      little in the way of toilet paper. Often, it is the left hand – with the aid of
      water – that is used for wiping. As a result, shaking hands or offering money with
      your left hand is considered rude, so make sure to use your “clean” right hand.</p>
